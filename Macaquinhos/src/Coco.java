public class Coco {
    private int pedrinhas;

    public Coco(int pedras){
        pedrinhas = pedras ;
    }
    

    /**
     * @return int return the pedrinhas
     */
    public int getPedrinhas() {
        return pedrinhas;
    }

    /**
     * @param pedrinhas the pedrinhas to set
     */
    public void setPedrinhas(int pedrinhas) {
        this.pedrinhas = pedrinhas;
    }

}
